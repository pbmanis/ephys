    def _data_complete_to_series(self, row):
        dc = row.data_complete.split(",")
        dc = [p.strip(" ") for p in dc if p != "nan" and "CCIV".casefold() in p.casefold()]
        # print("\ndc: ", dc)
        row.protocol = pd.Series(dc)
        # print(row.date, row.data_complete.values)
        return row

    def get_assembled_filename(self, experiment, mode:str="write"):
        """get_assembled_filename Create the filename for the assembled FI data.

        Parameters
        ----------
        experiment : _type_
            configuration file with paths
        mode : str
            "write" or "read"
            if "write", the filename will be modified to include the current date and time
            if a file of the same name exists.
            if "read", the filename will be returned as is without adding a timestamp.
        Returns
        -------
        _type_
            _description_
        """
        assert mode in ["write", "read"], "Mode must be 'write' or 'read'"
        assembled_fn = Path(
                experiment["analyzeddatapath"],
                experiment["directory"],
                experiment["assembled_filename"],
            )
        
        if mode == "read":
            print("Reading assembled data from: ", assembled_fn)
            return assembled_fn
        # avoid overwriting the assembled file if it already exists and is from a different date

        old_date = re.search(r"\d{4}.\d{2}.\d{2}", str(assembled_fn))
        if old_date is not None:
            old_date = old_date.group(0)
        else:
            old_date = "None"
        current_date = datetime.datetime.now().strftime("%Y.%m.%d")

        print("old date: ", old_date, current_date)

        if old_date != "None" and old_date != current_date:
            assembled_fn = str(assembled_fn).replace(old_date, current_date)
        else:
            assembled_fn = str(assembled_fn).replace(".pkl", f"_{current_date}.pkl")  # make new filename
        assembled_fn = Path(assembled_fn)
        if assembled_fn.is_file() and mode == "write":
            print("Assembled file already exists: ", assembled_fn)
            print("Will not overwrite, but will rename existing file adding a timestamp.")
            tstamp = assembled_fn.stat().st_mtime  # get the file's last modified time
            tstamp = datetime.datetime.fromtimestamp(tstamp).strftime("%H.%M.%S")
            new_name = str(assembled_fn).replace(".pkl", f"_{tstamp}.pkl")
            if not Path(new_name).is_file():
                print(f"Renaming existing file {assembled_fn} to: {new_name}")
                assembled_fn.rename(new_name)
        # print("Assembled filename: ", assembled_fn)
        # raise ValueError("Assembled filename: ", assembled_fn)
        return assembled_fn

    def assemble_datasets(
        self,
        df_summary: pd.DataFrame,
        fn: str = "",
        experiment: dict = None,
        exclude_unimportant: bool = False,
    ):
        """assemble_datasets : Assemble the datasets from the summary and coding files,
        then combine FI curves (selected) in IV protocols for each cell.
        We also calculate some spike rate measures
        We check for excluded spike analyses in the configuration file,
        so that the exclusions are honored.


        Parameters
        ----------
        df_summary : pd.DataFrame
            _description_
        fn : str, optional
            _description_, by default ""
        experiment : dict, optional
            _description_, by default None
             The experiment dictionary should have the following keys:
            - coding_file: str, the name of the coding file (excel) that has the coding information for the experiment.
            - coding_sheet: str, the name of the sheet in the coding file that has the coding information for the experiment.
            - coding_level: str, the level at which to apply the coding information (e.g., "date", "subject", "slice", "cell").
            - coding_name: str, the name of the column in the coding file that has the group information for the experiment.
        exclude_unimportant : bool, optional
            _description_, by default False


        """
        if experiment is None and self.experiment is None:
            raise ValueError(
                "Experiment not defined in AssembleData; should be done at time of the call"
            )
      
        print("Output will be written to: ", fn)

        self.experiment = experiment
        coding_file = experiment["coding_file"]
        coding_sheet = experiment["coding_sheet"]
        coding_level = experiment["coding_level"]
        coding_name = experiment["coding_name"]

        print(
            f"Assembling:\n  coding file: {coding_file!s}\n    Cells: {self.experiment['celltypes']!s}"
        )
        df = self.combine_summary_and_coding(
            df_summary=df_summary,
            coding_file=coding_file,
            coding_sheet=coding_sheet,
            coding_level=coding_level,
            coding_name=coding_name,
            exclude_unimportant=exclude_unimportant,
            status_bar=self.status_bar,
        )

        check_df_for_cells_trap(df, experiment, message="AssembleDatasets, after combining summary and coding")

        if "protocol" not in df.columns:
            df["protocol"] = ""
        # df = df.apply(self._data_complete_to_series, axis=1)  # Claude fixed 2026-07-16: replaced with vectorised version below
        # Vectorised replacement: split on comma, strip whitespace, keep only CCIV entries.
        # Result is a list per row; df.explode() below converts to one-row-per-protocol.
        df["protocol"] = (
            df["data_complete"]
            .str.split(",")
            .apply(lambda parts: [
                p.strip() for p in (parts or [])
                if p.strip() != "nan" and "cciv" in p.strip().lower()
            ])
        )
        # print(len(df), " rows after data complete to series")

        # now make a new dataframe that has a separate row for each protocol
        df = df.explode(["protocol"], ignore_index=True)
        print("Number of protocols after explode", len(df))
        df = df.dropna(subset=["protocol"])
        print("Number of protocols after dropna", len(df))

        df_null = df[df["cell_id"].isnull()]
        print("Null columns: ", df_null)
        df = df.dropna(subset=["cell_id"])
        print("# of protocols with ID: ", len(df))
        protostrings = "|".join(list(self.experiment["protocols"]["CCIV"].keys()))
        print("protostrings: ", protostrings)
        print("Protocols: ", df["protocol"].unique())
        # return
        # date = datetime.datetime.now().strftime("%d-%m-%Y")
        # fn_no_ext = Path(fn).stem
        # fn_parent = Path(fn).parent
        # fn_out = Path(fn_parent, f"{fn_no_ext}.pkl")
        check_df_for_cells_trap(df, experiment, message="AssembleDatasets, after exploding protocols")
        df = self.combine_by_cell(df)
        check_df_for_cells_trap(df, experiment, message="AssembleDatasets, after combining by cell")
        # print("\nWriting assembled data to : ", fn)
        fn_out = self.get_assembled_filename(self.experiment, mode="write")
        print("fnout: ", fn_out)
        print("fn: ", fn)
        print(df.head(10))
        print("Assembled data columns: ", df.columns)
        print("Assembled groups: dataframe Groups: ", df.Group.unique())
        df.to_pickle(fn, compression="gzip")

